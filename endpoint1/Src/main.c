/**
* @brief  The application entry point.
*
* @retval None
*/
int main(void)
{
  /* USER CODE BEGIN 1 */
  uint8_t mode = 0;
  /* USER CODE END 1 */
  
  /* MCU Configuration----------------------------------------------------------*/
  
  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  
  /* USER CODE BEGIN Init */
  
  /* USER CODE END Init */
  
  /* Configure the system clock */
  SystemClock_Config();
  
  /* USER CODE BEGIN SysInit */
  
  /* USER CODE END SysInit */
  
  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_USART1_UART_Init();
  MX_ADC_Init();
  MX_TIM6_Init();
  MX_RTC_Init();
  /* USER CODE BEGIN 2 */
  //低功耗模式下调试延时，如果不加很难再次刷程序
  HAL_Delay(5000);
  
  gUartDataPool = DataPoolInit(MAX_DMA_LEN);
  motor_init();
  
  LoraSetParamter( &gLoraPacket,
                  0x000f,
                  BAUD_57600,
                  CHAN_440MHZ,
                  PARITY_8O1,
                  SPEED_IN_AIR_19_2K,
                  TRANSFER_MODE_DINGDIAN,
                  WAKEUP_TIME_500MS );
  
  LoraReadParamter( &gLoraPacket );
  
  //CloseNotUsedPeriphClock();
  //串口重初始化
  UART_ReInit(&huart1);
  
  while(1) {
  
    if(LoraRegister())
      break;

    rtc_set_timer(7);
    
    LowPowerInit(1);
    HAL_PWR_EnterSTOPMode(PWR_LOWPOWERREGULATOR_ON, PWR_STOPENTRY_WFI);
    //时钟配置清空
    HAL_RCC_DeInit();
    //HSI时钟使能
    __HAL_RCC_DISABLE_IT(RCC_IT_HSIRDY);
    __HAL_RCC_HSI_ENABLE();
    while(!__HAL_RCC_GET_FLAG(RCC_FLAG_HSIRDY));
    //配置时钟和DMA
    SystemClock_Config();
    MX_DMA_Init();
    //串口重新配置
    UART_ReInit(&huart1);
//    HAL_Delay(1000);
  }
  /* USER CODE END 2 */
  
  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  
  while (1)
  {
    /* USER CODE END WHILE */
    
    /* USER CODE BEGIN 3 */
    //理想情况是，有数据的时侯aux会输出低电压
    //延迟2-3ms后数据??串口发送给mcu
    LowPowerInit(mode);
    HAL_PWR_EnterSTOPMode(PWR_LOWPOWERREGULATOR_ON, PWR_STOPENTRY_WFI);
    //while(gLoraPacket.isIdle);
    
    //时钟配置清空
    HAL_RCC_DeInit();
    //HSI时钟使能
    __HAL_RCC_DISABLE_IT(RCC_IT_HSIRDY);
    __HAL_RCC_HSI_ENABLE();
    while(!__HAL_RCC_GET_FLAG(RCC_FLAG_HSIRDY));
    //配置时钟和DMA
    SystemClock_Config();
    MX_DMA_Init();
    //串口重新配置
    UART_ReInit(&huart1);
    LoraModuleDMAInit( &gLoraPacket );

    //等待接收数据完成
    while(!gLoraPacket.isIdle);

    //调用数据处理
    UartDataProcess();
    
    //判断系统唤醒类型，分别是adc中断唤醒，电机检测引脚中断唤
    //和aux引脚中断唤醒
    if (mode) {
      //在这唤醒的话那么就执行发送函数发送回应的数据
      if ( SyncCMDDone() == 1) {
        mode = 0;
      }
    } else {
      mode = ProcessTheData();
    }
  }
  /* USER CODE END 3 */
  
}

